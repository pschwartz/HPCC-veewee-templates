start on runlevel [23]

respawn
exec agetty -h -L -w /dev/ttyS0 115200 vt102
EOM

kernel_version=$(uname -r)
dracut -a growroot -f /boot/initramfs-${kernel_version}.img
grubby --update-kernel=ALL --args=console=ttyS0,115200

exit 0
